let currentLang = localStorage.getItem('glh_lang') || 'en';

// Store original English text on nodes to allow switching back and forth
function storeOriginalText(node) {
  if (node.nodeType === Node.TEXT_NODE) {
    const text = node.textContent.trim();
    if (text.length > 0 && translations['en'][text]) {
      node._originalText = text;
    }
  } else if (node.nodeType === Node.ELEMENT_NODE) {
    // Some elements like buttons may just have text
    if (node.tagName !== 'SCRIPT' && node.tagName !== 'STYLE') {
      node.childNodes.forEach(storeOriginalText);
    }
  }
}

function translateNode(node, lang) {
  if (node.nodeType === Node.TEXT_NODE) {
    if (node._originalText && translations[lang][node._originalText]) {
      node.textContent = node.textContent.replace(node.textContent.trim(), translations[lang][node._originalText]);
    }
  } else if (node.nodeType === Node.ELEMENT_NODE) {
    if (node.tagName !== 'SCRIPT' && node.tagName !== 'STYLE') {
      node.childNodes.forEach(child => translateNode(child, lang));
    }
  }
}

function applyTranslation(lang) {
  translateNode(document.body, lang);
  
  // Update toggle button text if exists
  const langToggleText = document.getElementById('lang-toggle-text');
  if (langToggleText) {
    langToggleText.textContent = lang === 'en' ? 'EN' : 'DE';
  }
}

function toggleLanguage() {
  currentLang = currentLang === 'en' ? 'de' : 'en';
  localStorage.setItem('glh_lang', currentLang);
  applyTranslation(currentLang);
}

// Initialization
document.addEventListener('DOMContentLoaded', () => {
  storeOriginalText(document.body);
  
  if (currentLang !== 'en') {
    applyTranslation(currentLang);
  }
  
  // Bind toggle button
  const toggleBtn = document.getElementById('lang-toggle');
  if (toggleBtn) {
    toggleBtn.addEventListener('click', toggleLanguage);
  }
});
